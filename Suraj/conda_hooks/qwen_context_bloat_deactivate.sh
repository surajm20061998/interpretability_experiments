#!/usr/bin/env bash

if [ -n "${_QCB_PREV_HF_HOME+x}" ]; then
  export HF_HOME="${_QCB_PREV_HF_HOME}"
else
  unset HF_HOME
fi

if [ -n "${_QCB_PREV_HF_HUB_CACHE+x}" ]; then
  export HF_HUB_CACHE="${_QCB_PREV_HF_HUB_CACHE}"
else
  unset HF_HUB_CACHE
fi

if [ -n "${_QCB_PREV_TRANSFORMERS_CACHE+x}" ]; then
  export TRANSFORMERS_CACHE="${_QCB_PREV_TRANSFORMERS_CACHE}"
else
  unset TRANSFORMERS_CACHE
fi

if [ -n "${_QCB_PREV_MPLCONFIGDIR+x}" ]; then
  export MPLCONFIGDIR="${_QCB_PREV_MPLCONFIGDIR}"
else
  unset MPLCONFIGDIR
fi

if [ -n "${_QCB_PREV_TOKENIZERS_PARALLELISM+x}" ]; then
  export TOKENIZERS_PARALLELISM="${_QCB_PREV_TOKENIZERS_PARALLELISM}"
else
  unset TOKENIZERS_PARALLELISM
fi

if [ -n "${_QCB_PREV_PYTORCH_ENABLE_MPS_FALLBACK+x}" ]; then
  export PYTORCH_ENABLE_MPS_FALLBACK="${_QCB_PREV_PYTORCH_ENABLE_MPS_FALLBACK}"
else
  unset PYTORCH_ENABLE_MPS_FALLBACK
fi

if [ -n "${_QCB_PREV_PYTHONPATH+x}" ]; then
  export PYTHONPATH="${_QCB_PREV_PYTHONPATH}"
else
  unset PYTHONPATH
fi

if [ -n "${_QCB_PREV_QCB_PROJECT_ROOT+x}" ]; then
  export QCB_PROJECT_ROOT="${_QCB_PREV_QCB_PROJECT_ROOT}"
else
  unset QCB_PROJECT_ROOT
fi

if [ -n "${_QCB_PREV_QCB_ARTIFACT_DIR+x}" ]; then
  export QCB_ARTIFACT_DIR="${_QCB_PREV_QCB_ARTIFACT_DIR}"
else
  unset QCB_ARTIFACT_DIR
fi

unset _QCB_PREV_HF_HOME
unset _QCB_PREV_HF_HUB_CACHE
unset _QCB_PREV_TRANSFORMERS_CACHE
unset _QCB_PREV_MPLCONFIGDIR
unset _QCB_PREV_TOKENIZERS_PARALLELISM
unset _QCB_PREV_PYTORCH_ENABLE_MPS_FALLBACK
unset _QCB_PREV_PYTHONPATH
unset _QCB_PREV_QCB_PROJECT_ROOT
unset _QCB_PREV_QCB_ARTIFACT_DIR

